// Get Lenny episode URL for a guest
export function getLennyEpisodeUrl(guestName: string): string | null {
  const normalizedName = guestName.toLowerCase().trim();
  const episode = LENNY_EPISODES[normalizedName];
  if (episode) {
    return `https://www.youtube.com/watch?v=\${episode.videoId}`;
  }
  return null;
}

// Get Lenny episode info for a guest
export function getLennyEpisode(guestName: string): LennyEpisode | null {
  const normalizedName = guestName.toLowerCase().trim();
  return LENNY_EPISODES[normalizedName] || null;
}

// Check if a guest has been on Lenny's Podcast
export function hasLennyEpisode(guestName: string): boolean {
  const normalizedName = guestName.toLowerCase().trim();
  return normalizedName in LENNY_EPISODES;
}

// Search for episode by partial name match
export function searchLennyEpisodes(query: string): LennyEpisode[] {
  const normalizedQuery = query.toLowerCase().trim();
  return Object.values(LENNY_EPISODES).filter(ep => 
    ep.guest.toLowerCase().includes(normalizedQuery) ||
    ep.title.toLowerCase().includes(normalizedQuery)
  );
}

// Get direct YouTube URL or null - for use in podcast recommendations
export function getVerifiedEpisodeUrl(guestName: string, episodeTitle: string): string | null {
  // First try exact guest match
  const exactMatch = getLennyEpisodeUrl(guestName);
  if (exactMatch) return exactMatch;
  
  // Try searching by title keywords
  const titleWords = episodeTitle.toLowerCase().split(/\s+/);
  for (const [key, episode] of Object.entries(LENNY_EPISODES)) {
    const episodeTitleLower = episode.title.toLowerCase();
    // If multiple title words match, it's likely the right episode
    const matches = titleWords.filter(word => word.length > 3 && episodeTitleLower.includes(word));
    if (matches.length >= 2) {
      return `https://www.youtube.com/watch?v=\${episode.videoId}`;
    }
  }
  
  return null;
}

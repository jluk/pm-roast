// Get a random card for showcase
export function getRandomCelebrityCard(): CelebrityCard {
  return CELEBRITY_CARDS[Math.floor(Math.random() * CELEBRITY_CARDS.length)];
}

// Get card by ID
export function getCelebrityCardById(id: string): CelebrityCard | undefined {
  return CELEBRITY_CARDS.find(card => card.id === id);
}

// Search celebrity cards by name (case-insensitive fuzzy match)
export function searchCelebrityCards(query: string): CelebrityCard[] {
  const normalizedQuery = query.toLowerCase().trim();
  if (!normalizedQuery) return [];

  return CELEBRITY_CARDS.filter(card => {
    const name = card.name.toLowerCase();
    const company = card.company.toLowerCase();
    return name.includes(normalizedQuery) || company.includes(normalizedQuery);
  }).slice(0, 5);
}
